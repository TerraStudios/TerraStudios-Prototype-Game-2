using Unity.Mathematics;
using UnityEngine;

namespace TerrainGeneration
{
    /// <summary>
    /// Stores constant data about each voxel (cube)
    /// </summary>
    static class VoxelTables
    {
        /// <summary>
        /// Lookup table for the vertices in a cube
        /// </summary>
        public static readonly Vector3[] voxelVerts = new Vector3[8] {

        new Vector3(0.0f, 0.0f, 0.0f),
        new Vector3(1.0f, 0.0f, 0.0f),
        new Vector3(1.0f, 1.0f, 0.0f),
        new Vector3(0.0f, 1.0f, 0.0f),
        new Vector3(0.0f, 0.0f, 1.0f),
        new Vector3(1.0f, 0.0f, 1.0f),
        new Vector3(1.0f, 1.0f, 1.0f),
        new Vector3(0.0f, 1.0f, 1.0f),


    };

        /// <summary>
        /// Lookup table for the faces of a cube
        /// </summary>
        public static readonly int3[] faces = new int3[6]
        {
        new int3(0, 0, -1),
        new int3(0, 0, 1),
        new int3(0, 1, 0),
        new int3(0, -1, 0),
        new int3(-1, 0, 0),
        new int3(1, 0, 0)
        };

        /// <summary>
        /// Lookup table for the triangles of a cube
        /// </summary>
        public static readonly int[,] voxelTris = new int[6, 4] {

        {0, 3, 1, 2}, // Back Face
		{5, 6, 4, 7}, // Front Face
		{3, 7, 2, 6}, // Top Face
		{1, 5, 0, 4}, // Bottom Face
		{4, 7, 0, 3}, // Left Face
		{1, 2, 5, 6} // Right Face

	};

        /// <summary>
        /// Lookup table for the UVs of a cube
        /// </summary>
        public static readonly Vector2[] voxelUvs = new Vector2[4] {

        new Vector2 (0.0f, 0.0f),
        new Vector2 (0.0f, 1.0f),
        new Vector2 (1.0f, 0.0f),
        new Vector2 (1.0f, 1.0f)

    };
    }
}